package org.villaman.springtutorial;

public class FillPoints {
	private int pointX;
	private int pointY;
	
	public FillPoints(){}
	
	public int getPointX() {
		return pointX;
	}
	public void setPointX(int pointX) {
		this.pointX = pointX;
	}
	public int getPointY() {
		return pointY;
	}
	public void setPointY(int pointY) {
		this.pointY = pointY;
	}

}
